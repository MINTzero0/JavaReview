public class ChangeChar {
    public static void main(String[] args) {
        System.out.println("清风\t不时\t明月");
        System.out.println("------------");
        System.out.println("清风\n不时\n明月");
        System.out.println("------------");
        System.out.println("清风\\不时\\明月");
        System.out.println("------------");
        System.out.println("清风\"不时\'明月");
        System.out.println("------------");
        System.out.println("清风\r不时\r明月");
        System.out.println("------------");
    }
}
